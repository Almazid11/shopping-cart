function parsing(str) {
    var a = document.getElementById(str);
    var b = parseFloat(a);
    return b;
}
function Adding(num1,num2) {
    var p = parseFloat(num1);
    var q = parseFloat(num2);
    var r = p + q;
    return r;
}
function minus(num1,num2) {
    var p = parseFloat(num1);
    var q = parseFloat(num2);
    var r = p - q;
    return r;
}